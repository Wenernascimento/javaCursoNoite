package javaCurso2024;

public class If {
     public static void main (String[] args){
       int idade = 19;
       if (idade <= 20) {
    	   System.out.println("Menor que 20");
       } else if (idade > 20) {
    	   System.out.println("Maior que 20");

       }else {
    	   System.out.println("Igual a  20");

       }
       
     }
}
